        public static async Task Initialize(AppDbContext context)
        {
            if (await context.Peoples.AnyAsync()) return;


            foreach (var name in Names)
            {
                var splited = name.Split(" ", StringSplitOptions.RemoveEmptyEntries);
                await context.Peoples.AddAsync(new People {FirstName = splited[0], LastName = splited[1]});
            }
            await context.SaveChangesAsync();

            var peoples = await context.Peoples.ToListAsync();

            foreach (var people in peoples)
                await context.Marks.AddAsync(new Mark {Id = people.Id, Value = new Random().Next(100).ToString()});
            await context.SaveChangesAsync();
        }
